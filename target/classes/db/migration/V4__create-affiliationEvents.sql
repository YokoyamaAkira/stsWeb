CREATE TABLE affiliation_events (id INT PRIMARY KEY AUTO_INCREMENT,
                      event_date DATE DEFAULT CURRENT_TIMESTAMP(),
                      posting_date DATE DEFAULT DATEADD('DAY', 1, CURRENT_TIMESTAMP()),
                      posting_end_date DATE DEFAULT DATEADD('MONTH', 1, CURRENT_TIMESTAMP()),
                      title VARCHAR(100) DEFAULT '',
                      event_contents VARCHAR(100) DEFAULT '',
                      posting_end_f BOOLEAN DEFAULT false,
                      registration_date DATE DEFAULT CURRENT_TIMESTAMP(),
                      registered_person INT(255) DEFAULT 0,
                      edit_date DATE DEFAULT CURRENT_TIMESTAMP(),
                      editor INT(255) DEFAULT 0);